/* ========================================
            OPERADORES ARITMÉTICOS
   ======================================== */
let numero1 = 20;
let numero2 = 40;

/* ➤ 1: Suma numero1 y numero2. Muestra el resultado por consola. */
console.log('Suma: ', numero1 + numero2);
/* ➤ 2: Resta numero1 y numero2. Muestra el resultado por consola. */
console.log('Resta: ', numero1 - numero2);
/* ➤ 3: Multiplica numero1 y numero2. Muestra el resultado por consola. */
console.log('Multiplicacion; ', numero1 * numero2);
/* ➤ 4: Divide numero1 y numero2. Muestra el resultado por consola. */
console.log('Divide; ', numero1 / numero2);
/* ➤ 5: Saca el modulo entre numero2 y numero1. Muestra el resultado por consola. */
console.log('Modulo: ', numero2 % numero1);